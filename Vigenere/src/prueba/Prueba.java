	public static int buscaFila(char letra,char mat[][]) {
		int i=0,j=0;
		while(i<mat.length && mat[i][j]!=letra)
			i++;
		return i;
	}
	public static int buscaColumna(char letra,char mat[][]) {
		int i=0,j=0;
		while(j<mat.length && mat[i][j]!=letra)
			j++;
		return j;
	}
	

	public static char caracterTilde(char letra) {
		switch (letra) {
		case 'á':
			return 'a';
		case 'é':
			return 'e';

		case 'í':
			return 'i';

		case 'ó':
			return 'o';

		case 'ú':
			return 'u';

		case 'Á':
			return 'A';

		case 'É':
			return 'E';

		case 'Í':
			return 'I';

		case 'Ó':
			return 'O';

		case 'Ú':
			return 'U';

		default: return letra;
			
		}
	}
